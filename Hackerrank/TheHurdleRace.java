/*
The Hurdle Race problema de hackerrank

Problema:
En un juego se tiene un arreglo de alturas y una altura k que puede saltar un personaje para poder saltar mas se tiene que tomar una pocion que le suma 1 en altura. Cuantas pociones se debe tomar para poder saltar todo el arreglo

Soluci√≥n.
Se recorre el arreglo calculando la altura maxima y luego se devuelve la diferencia ente el salto y la altura maxima ( max - k) que representa la cantidad de pociones que se debe tomar
*/

class Result {

    /*
     * Complete the 'hurdleRace' function below.
     *
     * The function is expected to return an INTEGER.
     * The function accepts following parameters:
     *  1. INTEGER k
     *  2. INTEGER_ARRAY height
     */

    public static int hurdleRace(int k, List<Integer> height) {
    // Write your code here
    int max= k;
    for(int i : height){
        if(i>max)
            max=i;
        
    }
    return max-k;

    }

}

public class Solution {
    public static void main(String[] args) throws IOException {
        BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(System.getenv("OUTPUT_PATH")));

        String[] firstMultipleInput = bufferedReader.readLine().replaceAll("\\s+$", "").split(" ");

        int n = Integer.parseInt(firstMultipleInput[0]);

        int k = Integer.parseInt(firstMultipleInput[1]);

        List<Integer> height = Stream.of(bufferedReader.readLine().replaceAll("\\s+$", "").split(" "))
            .map(Integer::parseInt)
            .collect(toList());

        int result = Result.hurdleRace(k, height);

        bufferedWriter.write(String.valueOf(result));
        bufferedWriter.newLine();

        bufferedReader.close();
        bufferedWriter.close();
    }
}
